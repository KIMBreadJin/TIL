package com.green.dto;

public class Bus {
	private int address;
	private int phone;
	private int father;
	private int mother;
	private int son;
	private String name;
	public int getAddress() {
		return address;
	}
	public Bus setAddress(int address) {
		this.address = address;
		return this;
	}
	public int getPhone() {
		return phone;
	}
	public Bus setPhone(int phone) {
		this.phone = phone;
		return this;
	}
	public int getFather() {
		return father;
	}
	public Bus setFather(int father) {
		this.father = father;
		return this;
	}
	public int getMother() {
		return mother;
	}
	public Bus setMother(int mother) {
		this.mother = mother;
		return this;
	}
	public int getSon() {
		return son;
	}
	public Bus setSon(int son) {
		this.son = son;
		return this;
	}
	public String getName() {
		return name;
	}
	public Bus setName(String name) {
		this.name = name;
		return this;
	}
	
	
}
